---------------------------------------------------------
-- 1) 컬럼 추가
-- 목적: 학생 테이블에 ‘성별’ 컬럼을 CHAR(1) 타입으로 추가
ALTER TABLE 학생
ADD 성별 CHAR(1);

-- [예상 변경 후 컬럼 목록]
-- 학번 | 이름 | 생일 | 연락처 | 학과번호 | 성별

---------------------------------------------------------
-- 2) 컬럼 타입 수정
-- 목적: 방금 추가한 ‘성별’ 컬럼의 데이터 타입을 VARCHAR(2)로 변경
ALTER TABLE 학생
MODIFY COLUMN 성별 VARCHAR(2);

-- [예상 변경 후 컬럼 목록]
-- 학번 | 이름 | 생일 | 연락처 | 학과번호 | 성별 (VARCHAR(2))

---------------------------------------------------------
-- 3) 컬럼 이름 변경
-- 목적: ‘연락처’ 컬럼을 ‘휴대폰번호’로 이름 변경하고 타입은 VARCHAR(20) 유지
ALTER TABLE 학생
CHANGE COLUMN 연락처 휴대폰번호 VARCHAR(20);

-- [예상 변경 후 컬럼 목록]
-- 학번 | 이름 | 생일 | 휴대폰번호 | 학과번호 | 성별

---------------------------------------------------------
-- 4) 변경 사항 확인
-- 목적: 학생 테이블의 최종 구조 및 데이터를 조회
SELECT * FROM 학생;

---------------------------------------------------------
-- 5) 컬럼 삭제
-- 목적: 더 이상 필요 없는 ‘성별’ 컬럼을 제거
ALTER TABLE 학생
DROP COLUMN 성별;

-- [예상 변경 후 컬럼 목록]
-- 학번 | 이름 | 생일 | 휴대폰번호 | 학과번호

SELECT * FROM 학생;

---------------------------------------------------------
-- 6) 테이블 이름 변경
-- 목적: ‘휴학생’ 테이블 이름을 ‘졸업생’으로 변경
ALTER TABLE 휴학생
RENAME TO 졸업생;

-- [예상 테이블 목록]
-- 학과, 학생, 고객주문요약, 회원, 제품, 졸업생 등

---------------------------------------------------------
-- 7) 테이블 삭제
-- 목적: 더 이상 필요 없는 ‘학과’와 ‘학생’ 테이블을 완전히 제거
DROP TABLE 학과;
DROP TABLE 학생;

