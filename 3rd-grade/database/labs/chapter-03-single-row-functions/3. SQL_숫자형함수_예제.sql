-- ==================================================
-- SQL 수학 함수 정리
-- 이 파일은 수학 함수(CEILING, FLOOR, ROUND, TRUNCATE, ABS, SIGN, MOD, POWER, SQRT, RAND 등)를 포함합니다.
-- 각 섹션별로 SQL 구문을 설명하는 주석이 포함되어 있습니다.
-- ==================================================

-- [올림, 내림, 반올림, 버림 함수]
-- CEILING: 주어진 숫자보다 크거나 같은 가장 작은 정수를 반환합니다.
-- FLOOR: 주어진 숫자보다 작거나 같은 가장 큰 정수를 반환합니다.
-- ROUND: 주어진 숫자를 반올림합니다. 두 번째 인수를 지정하면 해당 소수점 자리까지 반올림합니다.
-- TRUNCATE: 주어진 숫자를 버림하여 지정한 소수점 자리까지 반환합니다.
SELECT 
    CEILING(123.56),        -- 결과: 124
    FLOOR(123.56),          -- 결과: 123
    ROUND(123.56),          -- 결과: 124
    ROUND(123.56, 1),       -- 결과: 123.6
    TRUNCATE(123.56, 1);    -- 결과: 123.5

-- [절대값 및 부호 함수]
-- ABS: 주어진 숫자의 절대값을 반환합니다.
-- SIGN: 숫자의 부호를 반환합니다. (음수: -1, 양수: 1, 0: 0)
SELECT 
    ABS(-120),  -- 결과: 120
    ABS(120),   -- 결과: 120
    SIGN(-120), -- 결과: -1
    SIGN(120);  -- 결과: 1

-- [나머지 함수]
-- MOD: 두 숫자를 나눈 나머지를 반환합니다.
SELECT 
    MOD(203, 4);  -- 결과: 3  (203 = 4*50 + 3)

-- [거듭제곱, 제곱근 및 난수 함수]
-- POWER: 거듭제곱을 계산합니다.
-- SQRT: 제곱근을 계산합니다.
-- RAND: 0과 1 사이의 난수를 반환합니다. (인수를 지정하면 해당 시드 기반 난수를 반환합니다)
-- 마지막 예제에서는 RAND()에 100을 곱하고 반올림하여 0에서 100 사이의 정수를 생성합니다.
SELECT 
    POWER(2, 3),         -- 결과: 8
    SQRT(16),            -- 결과: 4
    RAND(),              -- 결과: 0과 1 사이의 난수 (예: 0.345678)
    RAND(100),           -- 결과: 시드에 따른 난수 (예: 0.4321)
    ROUND(RAND() * 100);  -- 결과: 0에서 100 사이의 정수 (예: 42)
